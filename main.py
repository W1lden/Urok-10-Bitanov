import random
import pandas as pd


lst = ['robot'] * 10
lst += ['human'] * 10
random.shuffle(lst)
data = pd.DataFrame({'whoAmI':lst})
data.head()

for index, row in data.iterrows():
    if row["whoAmI"] == "human":
        row["whoAmI"] = 1
    elif row["whoAmI"] == "robot":
        row["whoAmI"] = 0

        
